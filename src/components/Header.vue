<template>
	<header>
		<h1>{{text}}</h1>
		<Button v-if="homePage" @btn-click="$emit('toggle-add-task')" :text="showAddTask ? 'Close' : 'Add Task'" :color="showAddTask ? 'red' : 'green'" />
	</header>
</template>

<script>
import Button from './Button';
export default {
	name: 'Header',
	props: {
		text: String,
		showAddTask: Boolean
	},
	components: {
		Button
	},
	computed: {
		homePage() {
			if(this.$route.path === '/') {
				return true
			} else {
				return false
			}
		}
	}
}
</script>

